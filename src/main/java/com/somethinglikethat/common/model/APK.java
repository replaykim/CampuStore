package com.somethinglikethat.common.model;

import javax.persistence.*;
import java.util.Date;

/**
 * Created by blue on 2017-04-12.
 */

@Entity
@Table(name = "APK_INFO_TB")
public class APK {
    @Id
    @GeneratedValue(strategy = GenerationType.AUTO)
    Integer APK_NO;
    String APK_NAME;
    String id;
    String VERSION_NO;
    String CAP_AMT;
    String STORAGE_URL;
    String SCREEN_URL;
    Integer CTGRY_NO;
    String DOWN_CNT;
    String VIEW_CNT;
    String RECM_CNT;
    String FUND_AMT;
    Date REG_DT;
    Date UPDT_DT;
    String APK_INFO;
    Integer APK_STAR;
    Integer FUND_GOAL;

    public Integer getAPK_NO() {
        return APK_NO;
    }

    public void setAPK_NO(Integer APK_NO) {
        this.APK_NO = APK_NO;
    }

    public String getAPK_NAME() {
        return APK_NAME;
    }

    public void setAPK_NAME(String APK_NAME) {
        this.APK_NAME = APK_NAME;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getVERSION_NO() {
        return VERSION_NO;
    }

    public void setVERSION_NO(String VERSION_NO) {
        this.VERSION_NO = VERSION_NO;
    }

    public String getCAP_AMT() {
        return CAP_AMT;
    }

    public void setCAP_AMT(String CAP_AMT) {
        this.CAP_AMT = CAP_AMT;
    }

    public String getSTORAGE_URL() {
        return STORAGE_URL;
    }

    public void setSTORAGE_URL(String STORAGE_URL) {
        this.STORAGE_URL = STORAGE_URL;
    }

    public String getSCREEN_URL() {
        return SCREEN_URL;
    }

    public void setSCREEN_URL(String SCREEN_URL) {
        this.SCREEN_URL = SCREEN_URL;
    }

    public Integer getCTGRY_NO() {
        return CTGRY_NO;
    }

    public void setCTGRY_NO(Integer CTGRY_NO) {
        this.CTGRY_NO = CTGRY_NO;
    }

    public String getDOWN_CNT() {
        return DOWN_CNT;
    }

    public void setDOWN_CNT(String DOWN_CNT) {
        this.DOWN_CNT = DOWN_CNT;
    }

    public String getVIEW_CNT() {
        return VIEW_CNT;
    }

    public void setVIEW_CNT(String VIEW_CNT) {
        this.VIEW_CNT = VIEW_CNT;
    }

    public String getRECM_CNT() {
        return RECM_CNT;
    }

    public void setRECM_CNT(String RECM_CNT) {
        this.RECM_CNT = RECM_CNT;
    }

    public String getFUND_AMT() {
        return FUND_AMT;
    }

    public void setFUND_AMT(String FUND_AMT) {
        this.FUND_AMT = FUND_AMT;
    }

    public Date getREG_DT() {
        return REG_DT;
    }

    public void setREG_DT(Date REG_DT) {
        this.REG_DT = REG_DT;
    }

    public Date getUPDT_DT() {
        return UPDT_DT;
    }

    public void setUPDT_DT(Date UPDT_DT) {
        this.UPDT_DT = UPDT_DT;
    }

    public String getAPK_INFO() {
        return APK_INFO;
    }

    public void setAPK_INFO(String APK_INFO) {
        this.APK_INFO = APK_INFO;
    }

    public Integer getAPK_STAR() {
        return APK_STAR;
    }

    public void setAPK_STAR(Integer APK_STAR) {
        this.APK_STAR = APK_STAR;
    }

    public Integer getFUND_GOAL() {
        return FUND_GOAL;
    }

    public void setFUND_GOAL(Integer FUND_GOAL) {
        this.FUND_GOAL = FUND_GOAL;
    }
}
